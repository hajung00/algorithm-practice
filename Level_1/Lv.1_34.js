function solution(s) {
  var answer = Array(s.length).fill(-1);
  const array = [...s];

  for (let i = s.length; i >= 0; i--) {
    for (let j = i - 1; j >= 0; j--) {
      if (array[i] === array[j]) {
        answer[i] = i - j;
        break;
      }
    }
  }
  return answer;
}

