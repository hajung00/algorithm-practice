function solution(a, b) {
  let arr = [a, b];
  arr.sort(function (a, b) {
    if (a > b) return 1;
    if (a === b) return 0;
    if (a < b) return -1;
  });

  let answer = Array(Math.abs(arr[1] - arr[0]) + 1)
    .fill(arr[0])
    .map((v, i) => v + i);
  let add = answer.reduce((acc, v) => acc + v);
  return add;
}

