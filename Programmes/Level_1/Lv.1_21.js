function solution(n) {
  var array = [];
  let answer = 0;
  let a = 1;
  while (n > 0) {
    array.push(n % 3);
    n = parseInt(n / 3);
  }

  for (let i = 0; i < array.length; i++) {
    for (let j = 0; j < array.length - i - 1; j++) {
      a *= 3;
    }
    answer += array[i] * a;
    a = 1;
  }
  return answer;
}

