function solution(food) {
  var answer = '';
  const array = food.map((item, i) => Math.floor(item / 2));

  for (let i = 1; i < array.length; i++) {
    for (let j = 0; j < array[i]; j++) {
      answer += i;
    }
  }
  const array_re = [...answer];
  array_re.reverse();
  return answer + 0 + array_re.join('');
}

