function solution(sizes) {
  var width = [];
  var height = [];

  sizes.map((size) => {
    if (size[0] < size[1]) {
      size = [size[1], size[0]];
    }
    width.push(size[0]);
    height.push(size[1]);
  });

  return Math.max(...width) * Math.max(...height);
}

