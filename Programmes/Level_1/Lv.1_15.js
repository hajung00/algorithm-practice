function solution(s) {
  var answer = [];
  let arr = s.split('');
  let number = arr.length;
  number % 2 === 0
    ? answer.push(arr[number / 2 - 1], arr[number / 2])
    : answer.push(arr[Math.floor(number / 2)]);
  return answer.join('');
}

