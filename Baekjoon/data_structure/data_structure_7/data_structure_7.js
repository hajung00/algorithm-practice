let fs = require("fs");
let input = fs.readFileSync("data_structure_7.txt").toString().split("\n");

let [n, k] = input[0].split(" ").map(Number);
let queue = Array(n)
  .fill()
  .map((v, i) => i + 1);

let answer = [];
let cnt = 1;

while (queue.length > 0) {
  if (cnt % k === 0) {
    answer.push(queue.shift());
  } else {
    queue.push(queue.shift());
  }
  cnt++;
}

console.log(`<${answer.join(", ")}>`);
