let fs = require("fs");
let input = fs.readFileSync("binary_search_3.txt").toString().split("\n");

let [n, count] = input[0].split(" ").map(Number);
let arr = input.map((v, i) => i !== 0 && v);
let answer = 0;

let start = 1;
let end = Math.max(...arr);

while (start <= end) {
  let mid = parseInt((start + end) / 2);
  let total = 0;
  for (let i = 1; i <= n; i++) {
    total += parseInt(input[i] / mid);
  }
  if (total < count) {
    end = mid - 1;
  } else {
    start = mid + 1;
    answer = mid;
  }
}
