let fs = require("fs");
let input = fs.readFileSync("backtracking_6.txt").toString().split("\n");

let [n, m] = input[0].split(" ").map(Number);
let arr = Array(n)
  .fill()
  .map((v, i) => i + 1);
let seleted = [];

function dfs(arr, start, depth) {
  if (m == depth) {
    console.log(seleted.toString().replaceAll(",", " "));
    return;
  }
  for (let i = start - 1; i < arr.length; i++) {
    seleted.push(i + 1);
    dfs(arr, i + 1, depth + 1);
    seleted.pop();
  }
}

dfs(arr, 1, 0);
