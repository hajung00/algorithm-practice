let fs = require("fs");
let input = fs.readFileSync("dynamic_11.txt").toString().split("\n");

let n = Number(input[0]);
let d = Array.from(Array(n + 1), () => Array(10).fill(0));

d[1][0] = 0;
for (let i = 1; i <= 9; i++) {
  d[1][i] = 1;
}

for (let i = 2; i <= n; i++) {
  for (let j = 0; j <= 9; j++) {
    d[i][j] = 0;
    if (j > 0) d[i][j] += d[i - 1][j - 1];
    if (j < 9) d[i][j] += d[i - 1][j + 1];
    d[i][j] %= Number(1e9);
  }
}

let result = 0;
for (let i = 0; i <= 9; i++) {
  result += d[n][i];
  result %= Number(1e9);
}
console.log(result);
