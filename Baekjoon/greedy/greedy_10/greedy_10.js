let fs = require("fs");
let input = fs.readFileSync("greedy_10.txt").toString().split("\n");

let n = Number(input[0]);
let arr = input[1].split(" ").map(Number);

let numArr = new Array(Math.max(...arr) + 1).fill().map((v, i) => [i, 0]);
let answer = 0;

for (let i = 0; i < n; i++) {
  if (numArr[arr[i]][1] === 0) {
    numArr[arr[i] - 1][1]++;
    answer++;
  } else {
    numArr[arr[i]][1]--;
    numArr[arr[i] - 1][1]++;
  }
}

console.log(answer);

