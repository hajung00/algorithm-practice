let fs = require("fs");
let input = fs.readFileSync("greedy_3.txt").toString().split("\n");

let arr = input[0].split("-");
let sumArr = arr.map((item) => {
  if (item.includes("+")) {
    let arr = item.split("+").map(Number);
    return arr.reduce((v, c) => v + c);
  } else return Number(item);
});

let answer = 0;
sumArr.forEach((item, i) => {
  if (i !== 0) answer -= item;
  else answer = item;
});
