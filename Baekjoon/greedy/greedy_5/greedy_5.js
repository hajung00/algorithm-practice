let fs = require("fs");
let input = fs.readFileSync("greedy_5.txt").toString().split("\n");

let [a, b] = input[0].split(" ").map(Number);
let answer = 1;
let flag = false;

while (b >= a) {
  if (a === b) {
    flag = true;
    break;
  } else if (b % 2 === 0) {
    b = parseInt(b / 2);
    answer++;
  } else if (b % 10 === 1) {
    b = parseInt(b / 10);
    answer++;
  } else break;
}

if (flag) console.log(answer);
else console.log(-1);
